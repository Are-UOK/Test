#include<stdio.h>

int i;      //全局变量-不初始化-默认是0
int main()
{
    i--;
    //-1
    //100000000000000000000000000000001
    //111111111111111111111111111111110
    //111111111111111111111111111111111
    if (i > sizeof(i))          //sizeof（）-计算变量/类型所占内存的大小 >=0 是无符号数
    {
        printf(">");            //这里会把-1二进制中的符号位去掉
    }
    else
    {
        printf("<");
    }
    
    return 0;
}