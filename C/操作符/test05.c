//隐式类型转换：表达式中的字符和短整型操作数在使用之前被转换为了普通整型，这种转换称为整型提升
#include<stdio.h>

int main()
{
    //整形提升
    char a = 3, b = 127;
    //00000000000000000000000000000011
    //00000011  - a
    //00000000000000000000000001111111
    //01111111  - b
    //a和b如何相加
    //00000011
    //01111111
    //00000000000000000000000000000011
    //00000000000000000000000001111111
    //00000000000000000000000010000010
    char c = a + b;
    //10000010 - c
    //11111111111111111111111110000010  - 补码
    //11111111111111111111111110000001  - 反码
    //00000000000000000000000001111110  - 原码
    //-126
    printf("%d\n", c);
    printf("------------------------------------------------------------\n");
    char x = 0xb6;      //10110110
    short y = 0xb600;
    int z = 0xb6000000;
    if (x == 0xb6)
    {
        printf("x");
    }
    if (y == 0xb600)
    {
        printf("y");
    }
    if (z == 0xb6000000)
    {
        printf("z");
    }
    printf("\n------------------------------------------------------------\n");
    //只要参与表达式运算，就会发生整形提升
    c = 1;
    printf("%u\n", sizeof(c));
    printf("%u\n", sizeof(+c));
    printf("%u\n", sizeof(!c));
    printf("------------------------------------------------------------\n");
    //算数转换
    float f = 3.14;
    int num = f;        //隐式转换，会有精度丢失
    return 0;
}